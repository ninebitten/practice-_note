# Addition
a = 5
b = 7
c = a + b
print(c)  # Output: 12

# Subtraction
x = 10
y = 3
z = x - y
print(z)  # Output: 7

# Multiplication
p = 3
q = 6
r = p * q
print(r)  # Output: 18

# Division
m = 20
n = 4
o = m / n
print(o)  # Output: 5.0 (Note: division always returns a float in Python)

# Modulus
e = 16
f = 3
g = e % f
print(g)  # Output: 1 (modulus returns the remainder of the division operation)
